# ---------------------------------------------------------------------------
# Batched generation — returns outputs AND per-prompt token counts
# ---------------------------------------------------------------------------

def _input_device(model) -> torch.device:
    return next(model.parameters()).device


def generate_batch(
    model,
    tokenizer,
    prompts: List[str],
    max_new_tokens: int,
) -> Tuple[List[str], List[Tuple[int, int]]]:
    """
    Returns:
        outputs: list of generated continuations (prompt stripped)
        token_stats: list of (tokens_input, tokens_generated) per prompt
    """
    if not prompts:
        return [], []

    inputs = tokenizer(prompts, return_tensors="pt", padding=True).to(_input_device(model))
    input_lengths = inputs["attention_mask"].sum(dim=1).tolist()  # actual (unpadded) input tokens

    with torch.no_grad():
        output_ids = model.generate(
            **inputs,
            do_sample=False,
            pad_token_id=tokenizer.eos_token_id,
            eos_token_id=tokenizer.eos_token_id,
            max_new_tokens=max_new_tokens,
        )

    padded_input_len = inputs["input_ids"].shape[1]
    eos_id = tokenizer.eos_token_id

    decoded = tokenizer.batch_decode(output_ids, skip_special_tokens=True)
    outputs = [text[len(prompt):].split("\n")[0].strip() for text, prompt in zip(decoded, prompts)]

    # Per-sample generated token count: exclude EOS and post-EOS padding (matches test.py approach).
    token_stats = []
    for i, il in enumerate(input_lengths):
        gen_portion = output_ids[i, padded_input_len:]
        n_gen = int((gen_portion != eos_id).sum())
        token_stats.append((int(il), n_gen))
    return outputs, token_stats


# ---------------------------------------------------------------------------
# Weight helpers
# ---------------------------------------------------------------------------

def _normalized_weights(particles: List[Particle]) -> np.ndarray:
    log_ws = np.array([p.log_weight for p in particles], dtype=np.float64)
    log_ws -= log_ws.max()
    ws = np.exp(log_ws)
    return ws / ws.sum()


# ---------------------------------------------------------------------------
# Core SMC particle filter
# ---------------------------------------------------------------------------

def systematic_resample(
    particles: List[Particle],
    active_idxs: List[int],
    rng: np.random.Generator,
) -> None:
    log_ws = np.array([particles[i].log_weight for i in active_idxs], dtype=np.float64)
    log_ws -= log_ws.max()
    ws = np.exp(log_ws)
    ws /= ws.sum()

    M = len(active_idxs)
    positions = (rng.random() + np.arange(M)) / M
    cumsum = np.cumsum(ws)
    chosen = np.searchsorted(cumsum, positions)

    new_particles = [copy.deepcopy(particles[active_idxs[j]]) for j in chosen]
    for p in new_particles:
        p.log_weight = 0.0
    for j, i in enumerate(active_idxs):
        particles[i] = new_particles[j]

def run_smc(
    maze: Maze,
    sys1_model,
    sys2_model,
    tokenizer,
    args,
    rng: np.random.Generator,
) -> dict:
    """
    SMC with dynamic model routing, best-of-trajectory step selection,
    revisit penalty, and stuck-triggered model switching.

    Returns result dict including a detailed per-step per-particle log.
    """
    M = args.M
    epsilon = args.epsilon
    alpha = args.alpha
    C = args.C
    alpha_temp = args.alpha_temp
    revisit_penalty = args.revisit_penalty

    particles: List[Particle] = []
    for _ in range(M):
        init_model = rng.choice(["sys1", "sys2"])
        particles.append(Particle(
            state=list(maze.start),
            history=[],
            model=init_model,
            log_weight=0.0,
            complete=(list(maze.start) == list(maze.goal)),
            states_explored=0,
            tokens_input=0,
            tokens_generated=0,
            visited={tuple(maze.start)},
            prev_state=None,
        ))

    step_logs = []

    for _t in range(args.Tmax):
        if all(p.complete for p in particles):
            break

        # Record model before transition for switch-tracking
        models_before_transition = [p.model for p in particles]

        # Model transition: force switch if stuck, else switch with prob epsilon
        for p in particles:
            if not p.complete:
                stuck = (p.prev_state is not None and p.state == p.prev_state)
                if stuck or rng.random() < epsilon:
                    p.model = "sys2" if p.model == "sys1" else "sys1"

        for p in particles:
            if not p.complete and p.tokens_generated > args.token_budget:
                p.complete = True
                p.failed = True

        step_particle_logs = []

        for model_type, model_obj in [("sys1", sys1_model), ("sys2", sys2_model)]:
            idxs = [i for i, p in enumerate(particles) if p.model == model_type and not p.complete]
            if not idxs:
                continue

            prompts = [build_prompt(maze, particles[i].state) for i in idxs]
            outputs, token_stats = generate_batch(model_obj, tokenizer, prompts, args.max_new_tokens)

            for idx_pos, (i, output, (tok_in, tok_gen)) in enumerate(zip(idxs, outputs, token_stats)):
                p = particles[i]
                state_before_move = list(p.state)

                actions = extract_full_plan_sys1(output) if model_type == "sys1" else extract_full_plan_sys2(output)
                p.states_explored += output.count("|") if model_type == "sys1" else output.count("Exploring")
                p.tokens_input += tok_in
                p.tokens_generated += tok_gen

                if actions:
                    traj_states, traj_actions = simulate_trajectory(maze, p.state, actions)
                    if len(traj_states) > 1:
                        rewards = [
                            compute_reward(maze, s, model_type, alpha, C)
                            for s in traj_states[1:]
                        ]
                        best_idx = int(np.argmax(rewards))
                        kept = traj_actions[:best_idx + 1]
                        p.history.extend(kept)
                        p.state = list(traj_states[best_idx + 1])
                        r = rewards[best_idx]
                    else:
                        traj_actions, kept = actions, []
                        r = compute_reward(maze, p.state, model_type, alpha, C)
                else:
                    traj_actions, kept = [], []
                    r = compute_reward(maze, p.state, model_type, alpha, C)

                revisit_applied = tuple(p.state) in p.visited
                if revisit_applied:
                    r -= revisit_penalty
                p.visited.add(tuple(p.state))
                p.prev_state = state_before_move

                if p.state == list(maze.goal):
                    p.complete = True

                p.log_weight += r / alpha_temp

                step_particle_logs.append({
                    "particle_id": i,
                    "model_before_switch": models_before_transition[i],
                    "model_used": model_type,
                    "switched": models_before_transition[i] != model_type,
                    "state_before": state_before_move,
                    "full_traj": traj_actions,
                    "kept": kept,
                    "fraction_kept": round(len(kept) / len(traj_actions), 3) if traj_actions else None,
                    "state_after": list(p.state),
                    "complete": p.complete,
                    "reward": round(r, 4),
                    "revisit_penalty_applied": revisit_applied,
                    "tokens_input": tok_in,
                    "tokens_generated": tok_gen,
                    "cumulative_tokens_input": p.tokens_input,
                    "cumulative_tokens_generated": p.tokens_generated,
                })

        step_logs.append({"step": _t, "particles": step_particle_logs})

        active_idxs = [i for i, p in enumerate(particles) if not p.complete]
        if len(active_idxs) > 1:
            systematic_resample(particles, active_idxs, rng)

    complete = [p for p in particles if p.complete and not p.failed]
    if complete:
        ws = _normalized_weights(complete)
        best = complete[int(np.argmax(ws))]
    else:
        def fallback_key(p: Particle) -> float:
            d = bfs_distance(maze, p.state, maze.goal)
            return d if d >= 0 else 1e9
        best = min(particles, key=fallback_key)

    plan_str = history_to_plan_str(maze.start, best.history)
    valid = maze.is_valid_plan(plan=plan_str, start=maze.start, goal=maze.goal)
    optimal = (
        maze.is_optimal_plan(plan=plan_str, check_validity=False, start=maze.start, goal=maze.goal)
        if valid else False
    )

    return {
        "valid": valid,
        "optimal": optimal,
        "plan": best.history,
        "plan_length": len(best.history),
        "states_explored": sum(p.states_explored for p in particles),
        "complete_particles": sum(1 for p in particles if p.complete and not p.failed),
        "completion_rate": sum(1 for p in particles if p.complete and not p.failed) / M,
        "total_tokens_input": sum(p.tokens_input for p in particles),
        "total_tokens_generated": sum(p.tokens_generated for p in particles),
        "per_particle_tokens": [
            {"particle_id": i, "tokens_input": p.tokens_input, "tokens_generated": p.tokens_generated}
            for i, p in enumerate(particles)
        ],
        "steps": step_logs,
    }


