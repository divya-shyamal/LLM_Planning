@dataclass
class Particle:
    state: list
    history: List[str]
    model: str
    log_weight: float
    complete: bool
    states_explored: int
    tokens_input: int = 0        # cumulative input tokens used by this particle
    tokens_generated: int = 0    # cumulative new tokens generated by this particle
    visited: set = field(default_factory=set)
    prev_state: Optional[list] = None
    failed: bool = False


# ---------------------------------------------------------------------------
# Maze helpers
# ---------------------------------------------------------------------------

def bfs_distance(maze: Maze, start: list, goal: list) -> int:
    if start == goal:
        return 0
    queue: deque = deque([(tuple(start), 0)])
    visited = {tuple(start)}
    while queue:
        (r, c), dist = queue.popleft()
        for action in maze.actions:
            ns = maze.execute_action([r, c], action)
            ns_t = tuple(ns)
            if ns_t not in visited and maze.is_valid_state(ns):
                if ns == goal:
                    return dist + 1
                visited.add(ns_t)
                queue.append((ns_t, dist + 1))
    return -1


def compute_reward(maze: Maze, state: list, model_type: str, alpha: float, C: float) -> float:
    d = bfs_distance(maze, state, maze.goal)
    if d < 0:
        return -1e6
    h = Maze.obstacles(state, maze.goal, maze.walls)
    alignment = -h if model_type == "sys1" else (h - C)
    return float(-d + alpha * alignment)


def apply_action(maze: Maze, state: list, action: str) -> list:
    ns = maze.execute_action(state, action)
    return ns if maze.is_valid_state(ns) else state


def history_to_plan_str(start: list, history: List[str]) -> str:
    parts = [f"start {start}"]
    parts += [f"{action} [0, 0]" for action in history]
    return " | ".join(parts)


# ---------------------------------------------------------------------------
# Trajectory simulation
# ---------------------------------------------------------------------------

def simulate_trajectory(maze: Maze, start: list, actions: List[str]) -> Tuple[List[list], List[str]]:
    """
    Simulate actions from start, skipping wall collisions and stopping at goal.
    """
    state = list(start)
    states = [state]
    valid_actions = []
    for action in actions:
        ns = apply_action(maze, state, action)
        if ns == state:
            continue
        states.append(list(ns))
        valid_actions.append(action)
        state = list(ns)
        if state == list(maze.goal):
            break
    return states, valid_actions


# ---------------------------------------------------------------------------
# Trajectory simulation (subgoal-aware variant for System-1.x)
# ---------------------------------------------------------------------------

def simulate_trajectory_to_subgoal(
    maze: Maze,
    start: list,
    actions: List[str],
    subgoal: list,
) -> Tuple[List[list], List[str]]:
    """
    Like simulate_trajectory but also stops when subgoal is reached.
    Used by run_system1x so that each sub-plan doesn't overshoot the subgoal.
    """
    state = list(start)
    states = [state]
    valid_actions = []
    for action in actions:
        ns = apply_action(maze, state, action)
        if ns == state:
            continue
        states.append(list(ns))
        valid_actions.append(action)
        state = list(ns)
        if state == list(maze.goal) or state == list(subgoal):
            break
    return states, valid_actions


